# Unlike other popular languages like C, C++, Java; Python doesn't support increment(++) and decrement(--) operators. 

# Augmented Assignment Operators: Augmented assignment operators combines an arithmetic or bitwise operator with assignment. In other words we can perform the assignment after a an operation. 

num = 5

num += 2       # num = num + 1         # Increments the value of the variable num by 1
num -= 1       # num = num - 1         # Derements the value of the variable num by 1
num *= 3       # num = num * 3         # Assigns the value of the variable after multiplying it's own value with 3
num /= 2       # num = num / 2         
num **= 2      # num = num ** 2

print(num)      