# NOTE: Typecasting is the process of converting a variable from one data type to another forcefully. 

name = "Xionex"
age = 20
sgpa = 8.7
is_registered = True

print(type(name))
print(type(age))
print(type(sgpa))
print(type(is_registered))


# TypeCasting sgpa from float type to int.
sgpa = int(sgpa)
print(type(sgpa))